function dark() {
  document.body.style.backgroundColor = "#121212";
  document.body.style.color = "#ffffff";
}

function light() {
  document.body.style.backgroundColor = "#ffffff";
  document.body.style.color = "#121212";
}

function semi_dark() {
  document.body.style.backgroundColor = "#414A4C";
  document.body.style.color = "#ffffff";
}
